#different type of tuples

# empty tuple
tup=()
print(tup)

# integer tuple
tuples=(1,2,3)
print(tuples)

#tuple with mixed data types
t=(1,'chikku',1.5)
print(t)

#nested tuple
b=('key',[1,2,3],(4,5,6)) # here list and a tuple is nested
print(b)

#tuple with one element
a=(6,)      #if there is only one element use "," , otherwise its considered as an integer
print(a)